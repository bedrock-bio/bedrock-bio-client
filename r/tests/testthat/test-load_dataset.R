fake_catalog <- list(dataset_a = "s3://bucket/a.json")

test_that("load_dataset errors on unknown dataset name", {
  local_mocked_bindings(
    get_catalog = function() fake_catalog,
    .package = "bedrockbio"
  )
  expect_error(
    load_dataset("not_real"),
    "not found in catalog"
  )
})

test_that("load_dataset passes catalog URL to iceberg_scan", {
  conn <- DBI::dbConnect(duckdb::duckdb())
  on.exit(DBI::dbDisconnect(conn, shutdown = TRUE))

  local_mocked_bindings(
    get_catalog = function() fake_catalog,
    get_connection = function() conn,
    .package = "bedrockbio"
  )

  # We can't run iceberg_scan without real data, but we can verify
  # the SQL that would be generated by checking the error message
  expect_error(
    load_dataset("dataset_a"),
    "iceberg_scan"
  )
})
